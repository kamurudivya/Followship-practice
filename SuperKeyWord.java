package org.example;

public class SuperKeyWord<lass> {
    lass Animal;{
        String color="white";
    }
    static class Dog extends Animal{
        String color="black";
        private boolean Animal;

        void printColor(){
            System.out.println(color);//prints color of Dog class
            System.out.println(Animal);//prints color of Animal class
        }
    }
    static class TestSuper1{
        public static void main(String args[]){
            Dog d=new Dog();
            d.printColor();
        }}
}
